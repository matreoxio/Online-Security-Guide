
 <h2>Passwords</h2>
 
 <p>
 Strong passwords have a number of qualities, these are discussed in this page.</p>
 
 <h3>Numbers</h3>
 <p>Passwords should contain at least one number. The more numbers the better. These should ideally not be the same, and not in succession.</p>
 
 <h3>Symbols</h3>
 <p>Passwords should contain at least one symbol. Try to use different symbols, and to mix-and-match symbols and other types of characters. Symbols include characters like '&#@*{}'.</p>
 
 <h3>Length</h3>
 <p>The longer the password the better, as long as you can remember it. Aim for passwords that are at least 16 characters long.</p>
 
 <h3>Uppercase Characters</h3>
 <p>Passwords should contain at least one uppercase character. These should mixed and matched with other types of characters and not in succession. </p>
 
 <h3>Lowercase Characters</h3>
 <p>Passwords should contain at least one lowercase character. These should mixed and matched with other types of characters and not in succession. </p>
 
 <h3>Words</h3>
 <p>Passwords should ideally NOT contain words that can be found in a dictionary. This is because hackers can automatically run these words in what is known as a 'Dictionary Attack' to try and break your password. </p>
 
 
