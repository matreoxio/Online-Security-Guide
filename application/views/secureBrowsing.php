<h1>Secure Browsing Knowledge</h1>
<h3>Turn Off Your Plugins </h3> 
<p>Deactivate plugins such as Active X, Cookies and Javascript and limit them to the websites you trust the most. 
This is because Hackers can detect security vulnerabilities within these plugins and use them to distribute malicious 
software while you are using them. Some browsers such as Google Chrome have functions that will let you whitelist the 
websites you trust to use these plugins securely. </p> 

<h3>Install An AdBlocker</h2>
<p>The is a an online threat known as Malvertising which means to use online adverts to distribute malware. 
Even legitimate online advertising has proven not to be safe and since there is a lot of adverts, the spread 
of malware thorugh these ads is also very large.  We recommend installing an adblocker such as Adblock plus, or Ublock Origin
in order to block these threats and have a cleaner browsing experience with less ads.</p> 

<h3>Ensure The Website is Using a Secure Url</h3> 
<p>If you are unsure that a website is secure then you can simply check the begining of the website's url located in the address bar. 
If that website is secure then you should notice that there is a green lock follow by "https" instead of the normal "http" and a grey lock which will be unlocked. 
Https stands for Hypertext Transfer Protocol Secure and website using this means that they agree to a "code" between it and the users computer that no can read 
the information you are inputting the website, ensuring the users safety from hackers. </p>

<h3>Use Different Passwords For Your Accounts</h3>
<p>Although it is a tedious task, we highly advise for users to use mutiple and different strong passwords for all their accounts that they access throught their browser. 
 This is because passwords can be compromised by hackers and if you have decied to use single password for mutiple accounts then there is a good chance that 
the hacker will use that password to access your other accounts to gather as much data on you as possible. </p>

<h3>Be Careful Using Public and Free Wireless Networks</h3>
<p> One method that online criminals use to capture data is to distribute wireless sniffers over unprotected wirless networks. The best way to use these networks 
safely is to broswe them while set to private browser mode. This will stop the sniffer from retieving any information you input as private browsing does not keep 
a record of the data you input into it.  </p>


