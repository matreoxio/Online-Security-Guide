<h1>General Security Knowledge</h1>
<h3>Use Complex Passwords</h3> 
<p>Create a strong and unique passwords for all your critical accounts, this is the best way to keep your personal
and financial information safe. Do not reuse these unique passwords as a single data breach can leave multiple of your accounts exposed.
We recommend that you use a password manager if you're having problems remembering all of these unique passwords.</p> 

<h3>Use a Firewall</h2>
<p>Even if your network is secure, we would still recommend a use of a firewall. This is a barrier that protects your computer/network
from external threats.</p> 

<h3>Be a Selective Sharer</h3> 
<p>Do not share any private information online as criminals may gain access to it and use it to impersonate you.</p>

<h3>Guard your Personal Information</h3>
<p>Try your best to avoid sharing information that can be used to identify and/or to impersonate your.
Such information includes- D.O.B, mothers maiden name, home address, national insurance number etc. </p>

<h3>Photos and videos</h3>
<p>Be careful about which photos and videos on social media sites that you use. Avoid photos/videos of your home,
work, school, private documents etc. </p>

<h3>Get Anti-Virus Software</h3>
<p>Make sure you have antivirus software installed and running on your computer. This will protect your computer from threats that get past your
firewall.</p>

<h3>Keep Up to Date</h3>
<p>Make sure that the software on your computer is always up to date, especially your antivirus, firewall and operating system software. These updates 
are used to fix bugs, security threats etc. so it is important that you install them as soon as they are available. </p>

<h3>Two-Factor Authentication</h3>
<p>Use 2FA whenever it is possible, this will give you an extra level of security and you will be notified whenever there is a
login attempt which will improve your response time.</p>

<h3>Public Wi-Fi</h3>
<p>Be wary of public Wi-Fi as criminals can use them to gain access to connected devices.</p>

<h3>Use an AdBlock</h3>
<p>Malicious software can spread via advertisements, use adblock to get rid of those risky advertisements while at the same time
improving your online experience.</p>

<h3>Privacy Settings</h3>
<p>Whenever available keep your privacy settings on, this will make it more difficult for criminals to collect information about you.</p>

